void housekeeping_task_user(void) {
    if (is_keyboard_master()) {
        if (syncState && transaction_rpc_send(SYNC_STATE, sizeof(pomo_state), &state)) {
            syncState = false;
        }
    } else {
        memcpy(header, headerBase, HEADER_SIZE);
        if (state == _WORK) {
            memcpy(statusDisplay, workBase, STATUS_SIZE);
        } else if (state == _REST) {
            memcpy(statusDisplay, restBase, STATUS_SIZE);
        }
    }
}

const char* getStateString(void) {
    switch (state) {
        case _STOPPED: return "STOPPED";
        case _REST: return "REST";
        case _WORK: return "WORK";
        default: return "UNKNOWN";
    }
}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
        // If you want to change the display of OLED, you need to change here
        oled_write_ln(read_layer_state(), false);
        oled_write_ln(getStateString(), false);
    } else {
        // if (state == _STOPPED) {
            // oled_write(read_logo(), false);
        // } else {
            oled_set_cursor(0, 0);
            oled_write_raw(header, HEADER_SIZE);
            oled_set_cursor(0, 1);
            oled_write_raw(statusDisplay, STATUS_SIZE);
        // }
    }
    return false;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed && keycode == KC_OPER) {
        state = (state + 1) % 3;
        syncState = true;
    }
    return true;
}
